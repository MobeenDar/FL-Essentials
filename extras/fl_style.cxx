//--------------------------------------------------------------- fl_style_parse
void fl_style_parse(int argc, const char** argv) {
    for (int f = 0; f < argc; f++) {
        if (strcmp("--black", argv[f]) == 0)
            fl_style_loadblack();
        else if (strcmp("--dark", argv[f]) == 0)
            fl_style_loaddark();
        else if (strcmp("--gray", argv[f]) == 0)
            fl_style_loadgray();
        else if (strcmp("--shake", argv[f]) == 0)
            fl_style_loadshake();
        else if (strcmp("--tan", argv[f]) == 0)
            fl_style_loadtan();
    }

    for (int f = 0; f < argc; f++) {
        if (strcmp("--base", argv[f]) == 0)
            Fl::scheme("base");
        else if (strcmp("--gtk", argv[f]) == 0)
            Fl::scheme("gtk+");
        else if (strcmp("--gleam", argv[f]) == 0)
            Fl::scheme("gleam");
        else if (strcmp("--plastic", argv[f]) == 0)
            Fl::scheme("plastic");
        else if (strcmp("--font1", argv[f]) == 0)
            fl_style_loadfont(FL_HELVETICA, FL_STYLE_FONT);
        else if (strcmp("--font2", argv[f]) == 0)
            fl_style_loadfont(FL_COURIER, FL_STYLE_FONTMONO);
        else if (strcmp("--font3", argv[f]) == 0)
            fl_style_loadfont(FL_HELVETICA, FL_STYLE_FONT, FL_COURIER, FL_STYLE_FONTMONO);
        else if (strcmp("--12", argv[f]) == 0)
            fl_style_fontsize(12);
        else if (strcmp("--14", argv[f]) == 0)
            fl_style_fontsize(14);
        else if (strcmp("--16", argv[f]) == 0)
            fl_style_fontsize(16);
        else if (strcmp("--18", argv[f]) == 0)
            fl_style_fontsize(18);
        else if (strcmp("--22", argv[f]) == 0)
            fl_style_fontsize(22);
        else if (strcmp("--28", argv[f]) == 0)
            fl_style_fontsize(28);
    }
}