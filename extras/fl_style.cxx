static Fl_Fontsize _FL_STYLE_FONT_SIZE = 14;

//------------------------------------------------------------ fl_style_fontsize
Fl_Fontsize fl_style_fontsize() {
    return _FL_STYLE_FONT_SIZE;
}

//------------------------------------------------------------ fl_style_fontsize
void fl_style_fontsize(Fl_Fontsize size) {
    _FL_STYLE_FONT_SIZE = size;
}

//------------------------------------------------------------ fl_style_fontsize
void fl_style_labelsize(Fl_Group* group) {
    group->labelsize(_FL_STYLE_FONT_SIZE);

    for (int f = 0; f < group->children(); f++) {
        Fl_Widget* child = group->child(f);
        child->labelsize(_FL_STYLE_FONT_SIZE);
    }
}

//------------------------------------------------------------ fl_style_loadfont
void fl_style_loadfont(Fl_Font font1, const char* name1, Fl_Font font2, const char* name2, Fl_Font font3, const char* name3, Fl_Font font4, const char* name4) {
    int         count = Fl::set_fonts(NULL);
    int         t     = 0;
    const char* name  = NULL;

    for (int f = 0; f < count; f++) {
        if (font1 == -1 && font2 == -1 && font3 == -1 && font4 == -1)
            break;

        name = Fl::get_font_name((Fl_Font) f, &t);
        // puts(name);

        if (font1 >= 0 && name1 && strcmp(name1, name) == 0) {
            Fl::set_font(font1, f);
            font1 = -1;
        }

        if (font2 >= 0 && name2 && strcmp(name2, name) == 0) {
            Fl::set_font(font2, f);
            font2 = -1;
        }

        if (font3 >= 0 && name3 && strcmp(name3, name) == 0) {
            Fl::set_font(font3, f);
            font3 = -1;
        }

        if (font4 >= 0 && name4 && strcmp(name4, name) == 0) {
            Fl::set_font(font4, f);
            font4 = -1;
        }
    }
}

